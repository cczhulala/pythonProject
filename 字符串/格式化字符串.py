#就是按一定格式输出的字符串
# %作为占位符，%s字符串，%i或者%d整数，%f浮点数   %10d，10是宽度，%.3f保留三位小数，还可以%10.3d同时精度宽度
#{}做占位符，{0}，{1}
#f-string

name = '张三'
age = 20
print('我叫%s,今年%10d岁' % (name,age))

print('{0:.3}'.format(3.1415926))#.3表示的一共三位数
print('{0:.3f}'.format(3.1415926))#.3f表示三位小数
print('{:.3f}'.format(3.1415926))#0可以不写
print('{:10.3f}'.format(3.1415926))#10表示宽度

print('我叫{0}，今年{1}岁'.format(name,age))

#f-string
print(f'我叫{name}，今年{age}岁')




