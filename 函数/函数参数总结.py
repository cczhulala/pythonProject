def fun(a,b,c):
    print('a=',a)
    print('b=',b)
    print('c=',c)
#函数调用是，注意，在参数定义和调用时是不一样的
fun(10,20,30)
lst = [11,22,33]
fun(*lst)#不加*报错，加*将列表中的每个元素都转换为位置实参传入

fun(a=100,b=200,c=300)
dict={'a':111,'b':222,'c':333}
fun(**dict)#加两个*，转化为关键字实参

#-------------------------------------------------
def fun1(a,b,*,c,d):#从*之后的参数在函数调用时只能使用关键字传递
    print(a)
    print(b)
    print(c)
    print(d)

fun1(10,20,c=30,d=40)